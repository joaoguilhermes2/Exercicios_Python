''' 15. Leia uma distância em milhas e apresente-a convertida em quilômetros. A fórmula de conversão 
é:  K = 1.61∗ M, sendo K a distância em quilômetros M em milhas. '''

distancia_milhas = float(input("Digite a distância em milhas: "))

Kilometro = 1.61 * distancia_milhas

print(f"A distância em milhas {distancia_milhas} convertida em quilômetros é {Kilometro}")